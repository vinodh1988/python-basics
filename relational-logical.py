# Demonstration of Relational and Logical Operators in Python

a = 10
b = 20

# Relational Operators
print("Relational Operators:")
print("a =", a, ", b =", b)
print("a > b:", a > b)
print("a < b:", a < b)
print("a == b:", a == b)
print("a != b:", a != b)
print("a >= b:", a >= b)
print("a <= b:", a <= b)

# Logical Operators
x = True
y = False

print("\nLogical Operators:")
print("x =", x, ", y =", y)
print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)
print("not y:", not y)

print("----------------------------------------------------")

